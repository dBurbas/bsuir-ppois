/**
 * @file publication.h
 * @brief Класс публикации (научной или учебной)
 * @author Dmitry Burbas
 * @date 16/12/2025
 */

#ifndef PUBLICATION_H
#define PUBLICATION_H

#include <string>

/**
 * @class Publication
 * @brief Простая структура данных для хранения информации о публикации
 * @details Представляет собой открытый контейнер (POD-like) для названия и
 * года.
 * @warning Внимание: Данный класс НЕ ОБЕСПЕЧИВАЕТ инкапсуляцию. Поля доступны
 * для прямого изменения извне без валидации. Использовать с осторожностью.
 */
class Publication {
 public:
  /**
   * @brief Название публикации
   * @details Может быть изменено напрямую. Проверка на пустоту отсутствует.
   */
  std::string title;

  /**
   * @brief Год публикации
   * @details Инициализируется нулем по умолчанию.
   * Может принимать отрицательные значения, что не блокируется классом.
   */
  int year = 0;
};

#endif  // PUBLICATION_H
